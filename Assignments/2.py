'''
Write a program to make addition, Subtraction, Multiplication and Division of
Two Numbers.
'''

first_number = float(input("Please enter the first number: "))
Second_number = float(input("Please enter the second number: "))

addition_results = first_number + Second_number
Subtraction_results = first_number - Second_number
Multiplication_results = first_number * Second_number
Division_results = first_number / Second_number
print("Addition of two numbers is: ", addition_results)
print("Subtraction of two numbers is: ", Subtraction_results)
print("Multiplication of two numbers is: ", Multiplication_results)
print("Division of two numbers is: ", Division_results)

